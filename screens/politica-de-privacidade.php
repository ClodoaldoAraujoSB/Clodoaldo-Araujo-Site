        <h3>Política de Privacidade da Super Business</h3>
        <br>
A Superbusiness (“nós”, “nosso” ou “nossa”) se preocupa com a proteção de suas informações pessoais. Esta Política de Privacidade explica como coletamos, usamos, divulgamos e protegemos suas informações quando você utiliza nossos serviços de cursos de empreendedorismo, tanto presenciais quanto a distância (os “Serviços”). Ao acessar ou utilizar os Serviços, você concorda com os termos desta Política de Privacidade.

1. Informações que Coletamos
Podemos coletar as seguintes informações pessoais quando você utiliza nossos Serviços:
1.1. Informações de Identificação Pessoal: Nome, endereço, número de telefone, endereço de e-mail, data de nascimento e outras informações que você fornece voluntariamente.
1.2. Informações de Pagamento: Informações relacionadas a transações financeiras, como números de cartão de crédito, dados bancários e outras informações necessárias para processar pagamentos.
1.3. Informações de Registro: Informações sobre sua conta, incluindo nome de usuário, senha e outras informações de segurança.
1.4. Informações de Uso: Dados sobre como você utiliza nossos Serviços, incluindo informações sobre os cursos que você acessa, as datas e horas de acesso, e seu progresso.
1.5. Informações de Comunicação: Registros de comunicações com nossa equipe de suporte ao cliente, bem como informações que você nos fornece ao preencher formulários de contato ou ao se comunicar conosco por e-mail, telefone ou chat.

2. Como Usamos Suas Informações
Utilizamos suas informações pessoais para os seguintes fins:
2.1. Fornecer Serviços: Para fornecer e administrar os cursos de empreendedorismo que você adquire conosco, incluindo o acesso a materiais didáticos, avaliações e suporte acadêmico.
2.2. Processar Pagamentos: Para processar pagamentos relacionados aos cursos e serviços que você adquire.
2.3. Comunicações: Para responder a suas perguntas, fornecer suporte ao cliente, enviar informações importantes sobre seus cursos, e comunicar atualizações e novidades sobre nossos Serviços.
2.4. Melhorar Nossos Serviços: Para analisar o uso de nossos Serviços e melhorar sua qualidade, personalização e eficácia.
2.5. Marketing: Para enviar informações sobre nossos cursos, eventos, promoções e ofertas especiais, desde que você tenha consentido em receber tais comunicações.

3. Compartilhamento de Informações
Não compartilharemos suas informações pessoais com terceiros, exceto nas seguintes situações:
3.1. Com Seu Consentimento: Quando obtivermos seu consentimento explícito para compartilhar suas informações.
3.2. Parceiros de Negócios: Com parceiros de negócios que auxiliam na prestação de nossos serviços, como processadores de pagamento e provedores de serviços de hospedagem.
3.3. Requisitos Legais: Quando formos obrigados a divulgar informações de acordo com a lei aplicável, regulamentos governamentais ou processos judiciais.

4. Segurança das Informações
Tomamos medidas razoáveis para proteger suas informações pessoais contra acesso não autorizado, uso indevido ou divulgação. No entanto, nenhum método de transmissão pela internet ou armazenamento eletrônico é 100% seguro, e não podemos garantir a segurança absoluta de suas informações.

5. Seus Direitos de Privacidade
Você tem direitos relacionados à privacidade de suas informações pessoais, incluindo o direito de acessar, corrigir e excluir seus dados. Para exercer esses direitos ou fazer qualquer pergunta sobre nossas práticas de privacidade, entre em contato conosco por meio das informações fornecidas na seção “Contato” abaixo.

6. Alterações a Esta Política de Privacidade
Podemos atualizar esta Política de Privacidade periodicamente para refletir mudanças em nossas práticas de privacidade. Quaisquer alterações significativas serão notificadas por meio de nosso site ou por outros meios de comunicação.

7. Contato
Se você tiver alguma dúvida ou preocupação sobre esta Política de Privacidade ou sobre suas informações pessoais, entre em contato conosco pelo seguinte endereço de e-mail: contato@superbusiness.com.br
Obrigado por escolher a Superbusiness. Estamos comprometidos em proteger sua privacidade e fornecer cursos de empreendedorismo de alta qualidade.</p>

